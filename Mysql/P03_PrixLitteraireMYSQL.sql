-- suppression des tables :
DROP TABLE IF EXISTS p03_RecompenserLaureatCarriere;
DROP TABLE IF EXISTS p03_RecompenserLaureatOeuvre;
DROP TABLE IF EXISTS p03_Ecrire;

DROP TABLE IF EXISTS p03_Livre;
DROP TABLE IF EXISTS p03_PrixCarriere;
DROP TABLE IF EXISTS p03_PrixOeuvre;
DROP TABLE IF EXISTS p03_Laureat;

-- creation de la table p03_Laureat
CREATE TABLE IF NOT EXISTS p03_Laureat(
    L_id INT NOT NULL AUTO_INCREMENT,
    L_nom VARCHAR(91) NOT NULL,
    L_prenom VARCHAR(50),
    L_sexe VARCHAR(15) CHECK (L_sexe IN ('masculin', 'feminin')),
    L_pays VARCHAR(60),
    PRIMARY KEY (L_id));

-- création de la table p03_Livre
CREATE TABLE IF NOT EXISTS p03_Livre (
    Liv_id INT NOT NULL AUTO_INCREMENT,
    Liv_titre VARCHAR(90) NOT NULL ,
    Liv_editeur VARCHAR(90) NOT NULL ,
    Liv_anneeEdition INT NOT NULL ,
    Liv_nbPages INT,
    PRIMARY KEY(Liv_id));

-- Création de la table p03_Oeuvre ( pour les prix récompensant une oeuvre )
CREATE TABLE IF NOT EXISTS p03_PrixOeuvre (
    P_Oeu_id INT NOT NULL AUTO_INCREMENT ,
    P_Oeu_nom varchar(90) NOT NULL ,
    P_Oeu_anneeDeSortie INT ,
    PRIMARY KEY (P_Oeu_id)
);

-- Création de la table p03_Carriere  ( pour les prix récompensant un écrivain pour l'ensemble de son oeuvre )
CREATE TABLE IF NOT EXISTS p03_PrixCarriere(
    P_C_id INT NOT NULL AUTO_INCREMENT ,
    P_C_nom varchar(90) NOT NULL ,
    P_C_anneeDeSortie INT NOT NULL ,
    PRIMARY KEY (P_C_id)
);

-- Création de la table associative p03_RecompenserLaureatOeuvre  ( pour associer chauque prix récompensant une oeuvre à son laureat )
CREATE TABLE IF NOT EXISTS p03_RecompenserLaureatOeuvre(
    L_id INT NOT NULL ,
    P_Oeu_id INT NOT NULL,
    date DATE ,
    PRIMARY KEY (L_id, P_Oeu_id),
    FOREIGN KEY (L_id) references p03_Laureat(L_id),
    FOREIGN KEY (P_Oeu_id) references p03_PrixOeuvre(p_Oeu_id)
);

-- Création de la table associative p03_RecompenserLaureatCarriere  ( pour associer chauque prix récompensant une carriere d'un écrivain à son laureat )
CREATE TABLE IF NOT EXISTS p03_RecompenserLaureatCarriere(
    L_id INT NOT NULL ,
    P_C_id INT NOT NULL,
    date DATE ,
    PRIMARY KEY (L_id, P_C_id),
    FOREIGN KEY (L_id) references p03_Laureat(L_id),
    FOREIGN KEY (P_C_id) references p03_PrixCarriere(p_C_id)
);


-- Création de la table associative p03_RecompenserOeuvre  ( pour associer chauque livre  à son prix )
CREATE TABLE IF NOT EXISTS p03_Ecrire(
    Liv_id INT NOT NULL ,
    L_id INT NOT NULL,
    PRIMARY KEY (Liv_id, L_id),
    FOREIGN KEY (Liv_id) references p03_Livre(Liv_id),
    FOREIGN KEY (L_id) references p03_Laureat(L_id)
);

